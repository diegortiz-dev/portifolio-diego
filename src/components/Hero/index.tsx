export default function Hero() {
  const [scrollY, setScrollY] = useState(0)
  
  const { displayText } = useTypewriter({
    words: HERO_ROLES,
    typingSpeed: 100,
    deletingSpeed: 50,
    pauseDuration: 2000,
  })

  // Efeito parallax
  useEffect(() => {
    const handleScroll = () => {
      setScrollY(window.scrollY)
    }
    window.addEventListener("scroll", handleScroll, { passive: true })
    return () => window.removeEventListener("scroll", handleScroll)
  }, [])

  const handleScrollTo = (e: React.MouseEvent<HTMLAnchorElement>, targetId: string) => {
    e.preventDefault()
    const targetElement = document.getElementById(targetId)
    
    if (targetElement) {
      const headerOffset = 80
      const targetPosition = targetElement.getBoundingClientRect().top + window.pageYOffset - headerOffset
      const startPosition = window.pageYOffset
      const distance = targetPosition - startPosition
      const duration = 1100
      let start: number | null = null

      const animation = (currentTime: number) => {
        if (start === null) start = currentTime
        const timeElapsed = currentTime - start
        const progress = Math.min(timeElapsed / duration, 1)
        
        const easeProgress = progress < 0.5
          ? 4 * progress * progress * progress
          : 1 - Math.pow(-2 * progress + 2, 3) / 2

        window.scrollTo(0, startPosition + distance * easeProgress)

        if (timeElapsed < duration) {
          requestAnimationFrame(animation)
        }
      }

      requestAnimationFrame(animation)
    }
  }

  return (
    <section className={styles.hero}>
      {/* Partículas interativas */}
      <Particles count={60} />
      
      {/* Efeitos de gradiente do fundo com parallax */}
      <div className={styles.background} aria-hidden="true">
        <div 
          className={styles.gradientOrb1} 
          style={{ transform: `translate(${scrollY * 0.1}px, ${scrollY * 0.05}px)` }}
        />
        <div 
          className={styles.gradientOrb2} 
          style={{ transform: `translate(${scrollY * -0.08}px, ${scrollY * 0.03}px)` }}
        />
        <div 
          className={styles.gradientOrb3} 
          style={{ transform: `translate(${scrollY * 0.05}px, ${scrollY * -0.02}px)` }}
        />
      </div>

      <div className={styles.container} style={{ transform: `translateY(${scrollY * 0.2}px)` }}>
        {/* Conteúdo principal */}
        <div className={styles.content}>
          <p className={styles.greeting}>Olá, meu nome é Diego</p>

          <h1 className={styles.title}>
            <span className={styles.titleMain}>DESENVOLVEDOR</span>
            <span className={styles.titleAccent}>DE SOFTWARE</span>
          </h1>

          <div className={styles.typewriter}>
            <span className={styles.typewriterLabel}>Meu objetivo é construir</span>
            <span className={styles.typewriterText}>
              {displayText}
              <span className={styles.cursor} aria-hidden="true">|</span>
            </span>
          </div>

          <div className={styles.ctaGroup}>
            <a 
              href="#contact" 
              className={styles.ctaButton}
              onClick={(e) => handleScrollTo(e, 'contact')}
            >
              Entrar em Contato
            </a>
            <a 
              href="#projects" 
              className={styles.ctaSecondary}
              onClick={(e) => handleScrollTo(e, 'projects')}
            >
              Ver Projetos
            </a>
          </div>
        </div>
      </div>

      {/* Seção Tech Stack */}
      <div className={styles.techStack}>
        <div className={styles.techStackHeader}>
          <span className={styles.bracket}>[</span>
          <h2 className={styles.techStackTitle}>Tech Stack</h2>
          <span className={styles.bracket}>]</span>
        </div>
        <div className={styles.techIcons}>
          {techIcons.map((tech) => (
            <div key={tech.name} className={styles.techIcon}>
              <svg viewBox="0 0 24 24" fill="currentColor" aria-label={tech.name}>
                <path d={tech.path} />
              </svg>
              <span className={styles.techTooltip}>{tech.name}</span>
            </div>
          ))}
        </div>
      </div>

      {/* Indicador de scroll */}
      <a href="#about" className={styles.scrollIndicator} aria-label="Scroll para próxima seção">
        <ChevronDownIcon size={24} />
      </a>
    </section>
  )
}
